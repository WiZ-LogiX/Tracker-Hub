#!/usr/bin/env node
// Regenerate src/lib/fonts/cairo.b64.ts from the bundled TTF files.
// Run after upgrading Cairo or replacing the TTF sources.
// Usage: `node scripts/build-font-b64.mjs`
import { readFileSync, writeFileSync } from "node:fs";
import { resolve, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const fontsDir = resolve(__dirname, "..", "src", "lib", "fonts");

const b64 = (name) => readFileSync(resolve(fontsDir, name)).toString("base64");

function wrap(s, w = 96) {
  return s.match(new RegExp(`.{1,${w}}`, "g")).join('" +\n  "');
}

const reg = b64("Cairo-Regular.ttf");
const sb = b64("Cairo-SemiBold.ttf");
const bo = b64("Cairo-Bold.ttf");

const header = `// Auto-generated base64 of the Cairo TTFs bundled in src/lib/fonts/.
// Generated by scripts/build-font-b64.mjs (do not edit by hand).
// These drive @react-pdf/renderer font registration without a network
// fetch at PDF render time, so PDFs keep working inside Cloudflare
// Workers (where raw fs reads need bundling first).\n\n`;

const out =
  header +
  `export const CAIRO_REGULAR_B64 =\n  "${wrap(reg)}";\n\n` +
  `export const CAIRO_SEMIBOLD_B64 =\n  "${wrap(sb)}";\n\n` +
  `export const CAIRO_BOLD_B64 =\n  "${wrap(bo)}";\n`;

writeFileSync(resolve(fontsDir, "cairo.b64.ts"), out);
console.log(`Wrote ${out.length} chars to src/lib/fonts/cairo.b64.ts`);
